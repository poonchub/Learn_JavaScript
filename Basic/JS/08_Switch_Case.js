let day = 1;

switch (day){
    case 1 :
        console.log("วันจันทร์")
        break
    case 2 :
        console.log("วันอังคาร")
        break
    case 3 :
        console.log("วันพุธ")
        break
    case 4 :
        console.log("วันพฤหัสบดี")
        break
    case 5 :
        console.log("วันศุกร์")
        break
    default :
        console.log("วันสุดสัปดาห์")
}


